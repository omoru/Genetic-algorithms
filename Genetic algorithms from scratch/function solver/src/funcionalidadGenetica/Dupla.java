package funcionalidadGenetica;

public class Dupla {
	
	private int puntero;
	private double valor;
	
	
	public Dupla (int puntero, double valor)
	{
		this.puntero=puntero;
		this.valor=valor;
	}
	
	public int getPuntero() {
		return puntero;
	}
	public void setPuntero(int puntero) {
		this.puntero = puntero;
	}
	public double getValor() {
		return valor;
	}
	public void setValor(double valor) {
		this.valor = valor;
	}

	

}
